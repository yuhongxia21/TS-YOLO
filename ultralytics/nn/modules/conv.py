class GhostModule(nn.Module):
    def __init__(self, inp, oup, kernel_size=1,stride=1, ratio=2, dw_size=5):
        super(GhostModule, self).__init__()
        self.oup = oup
        init_channels = math.ceil(oup / ratio)#1.Â∞ëÈáèÂç∑ÁßØÁöÑÈÄöÈÅì
        new_channels = init_channels*(ratio-1)#2.Ê∑±Â∫¶ÂèØÂàÜÁ¶ªÂç∑ÁßØÁöÑÈÄöÈÅì

        self.primary_conv = Conv(inp, init_channels, kernel_size, stride)# Â∞ëÈáèÂç∑ÁßØ

        self.cheap_operation = Conv(init_channels, new_channels, dw_size, 1, g=init_channels)

    def forward(self, x):
        x1 = self.primary_conv(x)
        x2 = self.cheap_operation(x1)
        out = torch.cat([x1,x2], dim=1)
        return out[:,:self.oup,:,:]

class Ghost(nn.Module):
    # Ghost Bottleneck https://github.com/huawei-noah/ghostnet
    def __init__(self, c1, c2, k=3, s=1):  # ch_in, ch_out, kernel, stride
        super(Ghost, self).__init__()
        c_ = c2 // 2
        self.conv = nn.Sequential(GhostModule(c1, c_, 1, 1),  # pw
                                  DWConv(c_, c_, k, s) if s == 2 else nn.Identity(),  # dw
                                  GhostModule(c_, c2, 1, 1))  # pw-linear
        self.shortcut = nn.Sequential(DWConv(c1, c1, k),
                                      GhostModule(c1, c2, 1, 1)) if s == 2 else nn.Identity()

    def forward(self, x):
        return self.conv(x) + self.shortcut(x)


class GhostBottleneck(nn.Module):
    # Standard bottleneck
    def __init__(
        self,
        in_channels,
        out_channels,
        shortcut=True,
        expansion=0.5,
        act="silu",
    ):
        super().__init__()
        hidden_channels = int(out_channels * expansion)
        self.conv1 = GhostModule(in_channels, hidden_channels, 1, 1)
        self.conv2 = GhostModule(hidden_channels, out_channels, 3,1)
        self.use_add = shortcut and in_channels == out_channels

    def forward(self, x):
        y = self.conv2(self.conv1(x))
        if self.use_add:
            y = y + x
        return y
